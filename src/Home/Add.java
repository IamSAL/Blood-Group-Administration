            pst.setString(4, jTextField4.getText());
            pst.setString(5, jTextArea1.getText());
            pst.setString(6, jTextField7.getText());
            pst.setString(7, jTextField6.getText());
            pst.execute();
            
            
            
            JOptionPane.showMessageDialog(null, "New Donor Added");
            
            
            
